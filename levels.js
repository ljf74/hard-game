function generateEnemySpinner(anchor, x, y, dx_arr, spl_arr, mul, dmul = 1, by_da = false) {
	var output = []

	for (var i = 0; i < dx_arr.length; i++) {
		for (var j = 0; j < (dx_arr[i] === 0 ? 1 : spl_arr.length); j++) {
			output.push({
				anchor: anchor,
				x: x,
				y: y,
				specials: { dx: dx_arr[i] * dmul, DA: (by_da ? (mul * spl_arr[j]) : 0) },
				delay: (by_da ? 0 : (mul * spl_arr[j]))
			})
		}
	}

	return output;
}

function generateAnchorCode(steps) {
	/**
	 * STEPS -> STEP[]
	 * 
	 * STEP -> OBJECT[]
	 *    DURATION: Integer 
	 *    POS: Function 
	 */
	var absoluteDuration = 0;

	for (var step of steps) {
		absoluteDuration += step[0]
	}

	//     var time = seconds % absoluteDuration 

	return {
		act: function(seconds, delay, ...args) {
			var time = (seconds + delay) % absoluteDuration;

			var total = 0;

			for (var step of steps) {
				total += step[0]

				if (time < total) {
					return step[1]((total - time) / step[0], ...args)
				}
			}
		}
	}
}

function LERP(SECONDS, P1, P2) {
	// LEAP => LERP(LEAP, P1, P2)
	return {
		x: P1[0] * SECONDS + P2[0] * (1 - SECONDS),
		y: P1[1] * SECONDS + P2[1] * (1 - SECONDS),
	} 
}

function ANGLED(SEC, SPEC, A1 = 0, A2 = 2 * Math.PI, REVERSED = false) {
	// (SEC, SPEC) => ANGLED(SEC, SPEC, A1?, A2?)
	var DY = SPEC.y || SPEC.dx;
	var DX = SPEC.x || 0;
	var a = (REVERSED ? -1 : 1) * (A1 * (1 - SEC) + A2 * SEC) + (SPEC.DA || 0);

	return {
		x: DY * Math.cos(a) + DX * Math.sin(a),
		y: DX * Math.cos(a) - DY * Math.sin(a)
	}
}

